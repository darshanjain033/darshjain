#Python program to convert kilometers to miles...
# 1 kilometer = 0.62137 miles 

# Miles = kilometer * 0.62137
Km = float(input("Enter Number in Km :"))
Miles = Km * 0.62137   #formula
print("%0.3f Km is equal to %0.3f Miles"%(Km,Miles))

#Miles to Kilometers...
# Kilometer = miles / 0.62137
miles = float(input("Enter Number in Miles :"))
Km = Miles / 0.62137   #formula
print("%0.3f Miles is equal to %0.3f km"%(Miles,Km))
